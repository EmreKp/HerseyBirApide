package com.emrekp.gunaydin.model;

import java.math.BigDecimal;

public class MainModel {

	private BigDecimal dolar;
	private BigDecimal euro;
	private BigDecimal pound;
	private BigDecimal altin;

	private Integer istanbul;
	private Integer ankara;
	private Integer izmir;

	public BigDecimal getDolar() {
		return dolar;
	}

	public void setDolar(BigDecimal dolar) {
		this.dolar = dolar;
	}

	public BigDecimal getEuro() {
		return euro;
	}

	public void setEuro(BigDecimal euro) {
		this.euro = euro;
	}

	public BigDecimal getPound() {
		return pound;
	}

	public void setPound(BigDecimal pound) {
		this.pound = pound;
	}

	public BigDecimal getAltin() {
		return altin;
	}

	public void setAltin(BigDecimal altin) {
		this.altin = altin;
	}

	public Integer getIstanbul() {
		return istanbul;
	}

	public void setIstanbul(Integer istanbul) {
		this.istanbul = istanbul;
	}

	public Integer getAnkara() {
		return ankara;
	}

	public void setAnkara(Integer ankara) {
		this.ankara = ankara;
	}

	public Integer getIzmir() {
		return izmir;
	}

	public void setIzmir(Integer izmir) {
		this.izmir = izmir;
	}
}
